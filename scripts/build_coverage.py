def validate_step2_batch(path: Path) -> list[str]:
    """Validate a Step 2 batch file has all required context.

    Returns a list of error strings. Empty list = valid.
    """
    errors = []
    try:
        data = json.loads(path.read_text())
    except (json.JSONDecodeError, OSError) as e:
        return [f"Cannot read/parse: {e}"]

    if not isinstance(data, dict):
        return [f"Top-level type is {type(data).__name__}, expected dict"]

    # Check required keys exist
    for key, label in STEP2_REQUIRED_FIELDS.items():
        if key not in data:
            errors.append(f"MISSING KEY: '{key}' ({label})")

    # Check non-empty values for critical text fields
    qa = data.get("qa_history", "")
    if not qa or (isinstance(qa, str) and len(qa.strip()) == 0):
        errors.append(f"EMPTY: qa_history — evaluator has no Q&A decision history")

    ap = data.get("anti_pattern_rules")
    if not ap:
        errors.append(f"EMPTY: anti_pattern_rules — evaluator has no anti-pattern rules")

    inv = data.get("chapter_inventory", [])
    if not inv or (isinstance(inv, list) and len(inv) == 0):
        errors.append(f"EMPTY: chapter_inventory — evaluator has no argument inventory")

    ct = data.get("chapter_text", "")
    if not ct or (isinstance(ct, str) and len(ct) < 100):
        errors.append(f"EMPTY/SHORT: chapter_text — {len(ct)} chars (need 100+)")

    findings = data.get("findings", [])
    if not findings or not isinstance(findings, list) or len(findings) == 0:
        errors.append(f"EMPTY: findings — nothing to evaluate")

    rules = data.get("step2_rules")
    if not rules:
        errors.append(f"EMPTY: step2_rules — evaluator has no verdict definitions")

    cross = data.get("cross_chapter_inventories")
    if not cross or (isinstance(cross, dict) and len(cross) == 0):
        errors.append(f"EMPTY: cross_chapter_inventories — cannot route wrong-chapter")

    # Check for WRONG key names (ad-hoc assembly artifacts)
    wrong_keys = {
        "qa_content": "qa_history",
        "anti_patterns": "anti_pattern_rules",
        "inventory": "chapter_inventory",
    }
    for wrong, correct in wrong_keys.items():
        if wrong in data and correct not in data:
            errors.append(
                f"WRONG KEY: '{wrong}' found — should be '{correct}'. "
                f"This batch was likely assembled ad-hoc, not by build_coverage.py"
            )

    return errors


def cmd_validate(args):
    """Validate Step 2 batch files have required context."""
    target = args.validate_path
    if not target:
        target = str(COVERAGE_DIR / "step2_batches")

    target_path = Path(target)
    if target_path.is_file():
        files = [target_path]
    elif target_path.is_dir():
        files = sorted(target_path.glob("*.json"))
        # Exclude verdict files
        files = [f for f in files if "verdict" not in f.name]
    else:
        print(f"ERROR: {target} not found", file=sys.stderr)
        return 1

    if not files:
        print(f"No batch files found in {target}")
        return 1

    total_errors = 0
    total_valid = 0
    for f in files:
        errors = validate_step2_batch(f)
        if errors:
            total_errors += 1
            print(f"\nFAIL: {f.name}")
            for e in errors:
                print(f"  - {e}")
        else:
            total_valid += 1
            print(f"OK: {f.name}")

    print(f"\n{'=' * 60}")
    print(f"VALID: {total_valid} | FAILED: {total_errors} | TOTAL: {len(files)}")
    if total_errors > 0:
        print(f"\nBLOCKED: {total_errors} batch(es) must not be evaluated.")
        print(f"Regenerate with: python scripts/build_coverage.py --embed-prep --chapter N")
        return 1
    else:
        print(f"\nAll batches valid. Safe to evaluate.")
        return 0


