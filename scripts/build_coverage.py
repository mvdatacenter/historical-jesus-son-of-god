RESEARCH_DIR = COVERAGE_DIR / "research_prep"


def _load_research_findings(ch: int) -> list[dict]:
    """Load Step 2 findings with verdict=RESEARCH for a chapter."""
    path = COVERAGE_DIR / f"ch{ch}_step2_verdicts.json"
    if not path.exists():
        return []

    data = json.loads(path.read_text())
    if not isinstance(data, list):
        return []

    research = [
        e for e in data
        if (e.get("verdict") or "").upper().strip() == "RESEARCH"
    ]

    # Enrich with finding text from extractions
    all_findings = parse_all_extractions()
    by_id = {f.finding_id: f for f in all_findings}
    out = []
    for e in research:
        fid = e.get("finding_id", "")
        f = by_id.get(fid)
        out.append({
            "finding_id": fid,
            "text": getattr(f, "text", "") if f else "",
            "step2_justification": e.get("justification", ""),
        })
    return out


def _build_research_prompt(ch: int, findings: list[dict],
                           inventory: list) -> str:
    """Assemble the literal prompt text to send to ChatGPT.

    Structure:
      1. Anti-verdict header (RESEARCH_PROMPT_HEADER)
      2. Anti-pattern rules (from ANTI_PATTERN_RULES)
      3. Chapter argument inventory (so GPT sees what the chapter argues)
      4. Findings list (each with finding_id, claim, why-it-survived)
      5. Required output structure (RESEARCH_OUTPUT_STRUCTURE)
    """
    parts = [RESEARCH_PROMPT_HEADER, ""]

    parts.append("=== Anti-pattern rules (apply to your output) ===\n")
    for i, rule in enumerate(ANTI_PATTERN_RULES, 1):
        parts.append(f"{i}. {rule}\n")
    parts.append("")

    parts.append(f"=== Chapter {ch} arguments (what the chapter is making) ===\n")
    parts.append("Use the bracketed section identifier as chapter_argument_id\n"
                 "in your evaluation artifact.\n")
    if inventory:
        for entry in inventory:
            arg_id = (entry.get("section")
                      or entry.get("argument_id")
                      or entry.get("id")
                      or "")
            summary = entry.get("argument") or entry.get("summary") or ""
            parts.append(f"- [{arg_id}] {summary}")
    else:
        parts.append("(no inventory available)")
    parts.append("")

    parts.append(f"=== Findings to enrich ({len(findings)}) ===\n")
    for f in findings:
        parts.append(f"finding_id: {f['finding_id']}")
        parts.append(f"  claim: {f['text']}")
        if f.get("step2_justification"):
            parts.append(f"  why_research: {f['step2_justification']}")
        parts.append("")

    parts.append("")
    parts.append(RESEARCH_OUTPUT_STRUCTURE)
    return "\n".join(parts)


def validate_research_prompt(prompt_text: str) -> list[str]:
    """Self-validate the assembled prompt before writing it.

    Mirrors validate_step2_batch (PM-0003): generation is allowed to fail,
    but never to produce a corrupt artifact.
    """
    errors = []

    # Anti-verdict header must be present verbatim
    if "NOT VERIFICATION" not in prompt_text:
        errors.append("MISSING: anti-verdict header")
    if "Do NOT assign verdicts" not in prompt_text:
        errors.append("MISSING: explicit no-verdicts directive")

    # Anti-pattern rules block must be present
    if "Anti-pattern rules" not in prompt_text:
        errors.append("MISSING: anti-pattern rules block")

    # Output structure must be present
    if "Required output structure" not in prompt_text:
        errors.append("MISSING: required output structure")

    # Findings list must be present
    if "finding_id:" not in prompt_text:
        errors.append("MISSING: findings (no finding_id: lines)")

    # Banned tokens — if these appear as instructions, the prompt is asking
    # for verdicts despite the header. Treat as a build error.
    banned_phrases = [
        "TRUE / FALSE",
        "TRUE/FALSE",
        "rate confidence",
        "give a verdict",
    ]
    for phrase in banned_phrases:
        # Phrase is allowed only inside the negative-example header block.
        # If it appears outside that block, fail.
        idx = prompt_text.find(phrase)
        if idx == -1:
            continue
        # Look back for the header marker — banned phrases inside the
        # "Do NOT" block are intended.
        preceding = prompt_text[max(0, idx - 200):idx]
        if "Do NOT" not in preceding:
            errors.append(
                f"BANNED PHRASE outside negative-example block: '{phrase}'"
            )

    return errors


def cmd_research_prep(args):
    """Assemble a research enrichment prompt for ChatGPT (PM-0004 #104).

    Modes:
      --research-prep --chapter N        # RESEARCH-verdict survivors
      --research-prep --batch FILE       # explicit batch file (list of
                                         # {finding_id, text, ...})

    Outputs (under sources/coverage/research_prep/):
      <batch_id>_prompt.txt    Literal text to send via `chatgpt send "$(cat …)"`
      <batch_id>_context.json  Machine-readable context — required by
                               --research-validate to know which findings
                               must appear in the eval artifact.
    """
    RESEARCH_DIR.mkdir(parents=True, exist_ok=True)

    if args.batch:
        batch_path = Path(args.batch)
        if not batch_path.exists():
            print(f"ERROR: {batch_path} not found", file=sys.stderr)
            return 1
        data = json.loads(batch_path.read_text())
        if not isinstance(data, list):
            print(f"ERROR: {batch_path} must be a JSON list of findings",
                  file=sys.stderr)
            return 1
        if not args.chapter:
            print("ERROR: --chapter required when using --batch "
                  "(needed to load chapter inventory)", file=sys.stderr)
            return 1
        ch = args.chapter
        findings = []
        for e in data:
            findings.append({
                "finding_id": e.get("finding_id", ""),
                "text": e.get("text", ""),
                "step2_justification": e.get("step2_justification",
                                              e.get("justification", "")),
            })
        batch_id = batch_path.stem
    elif args.chapter:
        ch = args.chapter
        findings = _load_research_findings(ch)
        batch_id = f"research_ch{ch}"
    else:
        print("ERROR: --research-prep requires --chapter N (and optionally "
              "--batch FILE)", file=sys.stderr)
        return 1

    if not findings:
        print(f"Ch{ch}: No RESEARCH-verdict findings to enrich.")
        return 0

    inventory = load_inventory(ch)
    prompt_text = _build_research_prompt(ch, findings, inventory)

    errors = validate_research_prompt(prompt_text)
    if errors:
        print(f"FAIL: assembled prompt is INVALID — refusing to write.",
              file=sys.stderr)
        for e in errors:
            print(f"  - {e}", file=sys.stderr)
        return 1

    prompt_path = RESEARCH_DIR / f"{batch_id}_prompt.txt"
    context_path = RESEARCH_DIR / f"{batch_id}_context.json"

    prompt_path.write_text(prompt_text)
    context_path.write_text(json.dumps({
        "batch_id": batch_id,
        "chapter": ch,
        "finding_ids": [f["finding_id"] for f in findings],
        "valid_argument_ids": [
            e.get("section") or e.get("argument_id") or e.get("id")
            for e in inventory
            if (e.get("section") or e.get("argument_id") or e.get("id"))
        ],
    }, indent=2, ensure_ascii=False))

    print(f"Ch{ch}: {len(findings)} findings → {prompt_path.name}")
    print(f"           context → {context_path.name}")
    print(f"\nNext steps:")
    print(f"  1. Send the prompt:")
    print(f"       chatgpt send \"$(cat {prompt_path})\"")
    print(f"  2. Save GPT's response, then write the eval artifact at:")
    print(f"       {RESEARCH_DIR / (batch_id + '_eval.json')}")
    print(f"  3. Validate before presenting findings:")
    print(f"       python scripts/build_coverage.py --research-validate "
          f"--batch-id {batch_id}")
    return 0


def validate_research_eval(eval_path: Path,
                           context_path: Path) -> list[str]:
    """Validate a research-eval.json against the batch context.

    Required: the artifact maps every batch finding to a chapter argument
    with a final verdict before any presentation. See PM-0004 #105.
    """
    errors = []

    if not eval_path.exists():
        return [f"NOT FOUND: {eval_path} — write the eval artifact first"]
    if not context_path.exists():
        return [f"NOT FOUND: {context_path} — re-run --research-prep"]

    try:
        ctx = json.loads(context_path.read_text())
    except json.JSONDecodeError as e:
        return [f"context not parseable: {e}"]

    try:
        eval_data = json.loads(eval_path.read_text())
    except json.JSONDecodeError as e:
        return [f"eval not parseable: {e}"]

    if not isinstance(eval_data, dict):
        return [f"eval top-level type is {type(eval_data).__name__}, expected dict"]

    if eval_data.get("batch_id") != ctx.get("batch_id"):
        errors.append(
            f"batch_id mismatch: eval={eval_data.get('batch_id')!r} vs "
            f"context={ctx.get('batch_id')!r}"
        )

    findings = eval_data.get("findings")
    if not isinstance(findings, list) or not findings:
        return errors + ["MISSING: 'findings' list (non-empty)"]

    expected_ids = set(ctx.get("finding_ids", []))
    valid_arg_ids = set(ctx.get("valid_argument_ids", []))

    seen_ids = set()
    for i, entry in enumerate(findings):
        if not isinstance(entry, dict):
            errors.append(f"findings[{i}]: not an object")
            continue
        fid = entry.get("finding_id", "")
        seen_ids.add(fid)

        for key in RESEARCH_REQUIRED_FIELDS:
            if key not in entry:
                errors.append(f"findings[{i}] ({fid}): MISSING field '{key}'")

        verdict = (entry.get("verdict") or "").strip().lower()
        if verdict and verdict not in RESEARCH_VERDICTS:
            errors.append(
                f"findings[{i}] ({fid}): invalid verdict {verdict!r}, "
                f"must be one of {RESEARCH_VERDICTS}"
            )

        arg_id = entry.get("chapter_argument_id", "")
        if arg_id and valid_arg_ids and arg_id not in valid_arg_ids:
            errors.append(
                f"findings[{i}] ({fid}): chapter_argument_id "
                f"{arg_id!r} not in chapter inventory"
            )

        what_adds = (entry.get("what_finding_adds") or "").strip()
        if not what_adds:
            errors.append(
                f"findings[{i}] ({fid}): EMPTY 'what_finding_adds' — "
                f"argument-mapping requires this"
            )

        justification = (entry.get("verdict_justification") or "").strip()
        if not justification:
            errors.append(
                f"findings[{i}] ({fid}): EMPTY 'verdict_justification'"
            )

    missing = expected_ids - seen_ids
    if missing:
        errors.append(
            f"MISSING findings (in batch but not in eval): "
            f"{sorted(missing)}"
        )

    extra = seen_ids - expected_ids - {""}
    if extra:
        errors.append(
            f"UNEXPECTED findings (in eval but not in batch): "
            f"{sorted(extra)}"
        )

    return errors


def cmd_research_validate(args):
    """Validate research-eval.json artifact (PM-0004 #105).

    No presentation summary is emitted unless validation passes. This is
    the forcing function that prevents ad-hoc paraphrase of GPT output:
    the only sanctioned summary comes from this command.
    """
    if not args.batch_id:
        print("ERROR: --research-validate requires --batch-id NAME",
              file=sys.stderr)
        return 1

    eval_path = RESEARCH_DIR / f"{args.batch_id}_eval.json"
    context_path = RESEARCH_DIR / f"{args.batch_id}_context.json"

    errors = validate_research_eval(eval_path, context_path)
    if errors:
        print(f"\nFAIL: {eval_path.name}")
        for e in errors:
            print(f"  - {e}")
        print(f"\nBLOCKED: research findings cannot be presented until the")
        print(f"eval artifact is complete. See PM-0004.")
        return 1

    eval_data = json.loads(eval_path.read_text())
    findings = eval_data["findings"]

    print(f"OK: {eval_path.name} — {len(findings)} findings evaluated\n")

    by_verdict = defaultdict(list)
    for e in findings:
        by_verdict[e.get("verdict", "")].append(e)

    print("=" * 60)
    print(f"Research findings — batch {args.batch_id}")
    print("=" * 60)
    for verdict in RESEARCH_VERDICTS:
        items = by_verdict.get(verdict, [])
        if not items:
            continue
        print(f"\n[{verdict}] ({len(items)})")
        for e in items:
            print(f"  - {e['finding_id']}  →  {e['chapter_argument_id']}")
            print(f"      adds: {e['what_finding_adds']}")
            print(f"      why:  {e['verdict_justification']}")
    print()
    return 0


